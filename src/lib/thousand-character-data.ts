// 获取所有千字文字符
export const getAllThousandCharCharacters = (): ThousandCharCharacter[] => {
  return thousandCharSections.flatMap(section => section.characters);
};

// 根据难度获取字符
export const getThousandCharByLevel = (level: 'basic' | 'intermediate' | 'advanced'): ThousandCharCharacter[] => {
  return getAllThousandCharCharacters().filter(char => char.level === level);
};

// 搜索字符
export const searchThousandChar = (keyword: string): ThousandCharCharacter[] => {
  const chars = getAllThousandCharCharacters();
  return chars.filter(char =>
    char.char.includes(keyword) ||
    char.pinyin.includes(keyword) ||
    char.words.some(word => word.includes(keyword))
  );
};
